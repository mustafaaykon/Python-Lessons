# .capitalize() => parametrede verilen değerin ilk harifini büyük olarak değiştirir


isim = "murat vuranok"
print(isim.capitalize())  # Murat vuranok

# .title() => parametrede verilen değerin içerisindeki her bir kelimenin ilk harfini büyük olarak teslim eder.
print(isim.title())  # Murat Vuranok
